valor1 = 10
valor2= 20

#Adição
print("adição", valor1 + valor2)
#Subtração
print("subtração", valor1 - valor2)
#Multiplicação
print("multiplicação", valor1 * valor2)
#Divisão
print("divisão", valor1 / valor2)
#Exponenciação
print("exponenciação", valor1 ** valor2)
#Módulo
print("módulo", valor1 % valor2)

#Conversões
print(1 + int('2'))
print(1 + float('2.5'))
print( str(1) + '2')

